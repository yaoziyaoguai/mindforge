"""M5.3 后续 — 多 project 联合上下文渲染（**安全拼装，非 RAG**）。

为什么"多 project context"仍然不是 RAG：

- 数据源仍然只是 ``20-Knowledge-Cards/`` 与 ``30-Projects/<name>.md``
  frontmatter；**不**做 embedding、**不**算相似度、**不**调 LLM；
- 所谓"联合"只是把多个项目共享的卡片做 **集合并 + path-level 去重**，
  按"项目 → 卡片"关系结构化分组输出；
- suggested prompt 是固定模板拼装，明确告诉下游 agent："你拿到的是
  多项目上下文包，原则可能并存且**未自动裁决**"。

输出结构（与单 project 渲染器互补）：

1. Project Overview            — 项目计数 + 名称列表 + target
2. Source Notice               — 每个项目的 profile_found / 路径
3. Project Profiles            — 每个项目的 description / preferred_workflow
4. Cross-project Learning Tracks — 跨项目卡片的 track 累计
5. Cross-project Principles    — 项目级 + 卡片级；按 (project / card) 来源标注
6. Cross-project Known Risks   — 同上
7. Project-specific Cards      — 按项目分组；卡片去重，归入第一个匹配项目
8. Shared Action Items         — 跨项目的 action_items（按卡片合并）
9. Review Due                  — 跨项目复习清单
10. Suggested Prompt for <target> — 注明这是 multi-project context pack
11. Excluded Content           — 与单项目一致的安全清单
"""

from __future__ import annotations

import json
from datetime import datetime, timezone

from .cards import CardSummary, extract_section
from .project_context import (
    EXCLUDED_CONTENT,
    ProjectContextOptions,
    _d,
    _due_now,
    _overdue_days,
    _safe_body,
    _select,
    _TARGET_STYLE,
)
from .project_profile import ProjectProfile


def render_multi_project_context_markdown(
    project_names: list[str],
    cards_by_project: dict[str, list[CardSummary]],
    profiles: dict[str, ProjectProfile],
    *,
    options: ProjectContextOptions,
) -> str:
    """多 project markdown 渲染（thin orchestrator）。

    架构边界（Residual Architecture Debt Closure Pack）：

    - 这里只负责 **section 顺序编排** 与公共上下文（``selected_by_project``、
      ``all_selected``、``now_iso``）准备；
    - 11 个具名段落各自下沉到 ``_section_*`` 私有 helper，每个 helper 单一
      职责、易于阅读与回归测试；
    - 不变量：所有 section 的 header / 内容 / 顺序 / 可选开关与原实现完全
      一致；外部行为零变化；
    - ``cards_by_project`` 的 status / drafts 过滤仍由调用方负责。
    """
    assert len(project_names) >= 2, "multi-project 渲染至少需要 2 个项目"

    selected_by_project = _select_and_dedup(project_names, cards_by_project, options)
    all_selected = [c for cs in selected_by_project.values() for c in cs]
    now_iso = datetime.now(timezone.utc).astimezone().isoformat(timespec="seconds")

    lines: list[str] = []
    lines.extend(_section_overview(project_names, options, now_iso))
    lines.extend(_section_source_notice(project_names, profiles, selected_by_project))
    lines.extend(_section_project_profiles(project_names, profiles))
    lines.extend(_section_cross_tracks(all_selected))
    lines.extend(_section_cross_principles(project_names, profiles, selected_by_project))
    lines.extend(_section_cross_risks(project_names, profiles, selected_by_project))
    lines.extend(_section_project_specific_cards(project_names, selected_by_project))
    if options.include_actions:
        lines.extend(_section_shared_action_items(all_selected))
    if options.include_review_due:
        lines.extend(_section_review_due(all_selected))
    if options.include_next_step_prompt:
        lines.extend(_section_suggested_prompt(project_names, profiles, options))
    lines.extend(_section_excluded_content())

    return "\n".join(lines).rstrip() + "\n"


# ---------------------------------------------------------------------------
# Section helpers — 每个返回 list[str]（不含尾部换行；由 orchestrator 用 \n join）。
# 拆分理由：原 178 行 god-renderer 同时承担 11 个独立段落 + 数据准备 + 排序，
# 违反单一职责。helper 化后：
#   * 每个段落独立可读、可单测；
#   * orchestrator 退化为段落顺序声明，便于审计 section 顺序与可选段。
# 不变量（与原实现严格一致）：header 字面、可选段开关、顺序、空态文案。
# ---------------------------------------------------------------------------


def _section_overview(
    project_names: list[str],
    options: ProjectContextOptions,
    now_iso: str,
) -> list[str]:
    """1. Project Overview — 标题 + 生成时间 + 项目列表 + drafts 提示。"""
    lines = [
        f"# Multi-Project Context · {' · '.join(project_names)}\n",
        f"> Generated by mindforge project context at {now_iso}",
        f"> projects: {project_names} (count={len(project_names)})",
        f"> target: {options.target}",
    ]
    if options.include_drafts:
        lines.append("> 包含 ai_draft 卡片（--include-drafts）")
    else:
        lines.append("> 仅含 status=human_approved 的卡片摘要")
    lines.append("> 多项目模式：来源透明、原则不自动裁决（详见 Cross-project Principles）\n")
    return lines


def _section_source_notice(
    project_names: list[str],
    profiles: dict[str, ProjectProfile],
    selected_by_project: dict[str, list[CardSummary]],
) -> list[str]:
    """2. Source Notice — 每个项目的 profile 来源 + 去重后总卡数。"""
    lines = ["## Source Notice"]
    for name in project_names:
        prof = profiles[name]
        if prof.found:
            lines.append(f"- {name}: project_profile_found=true ({prof.rel_path})")
        else:
            lines.append(
                f"- {name}: project_profile_found=false (using Knowledge Cards only)"
            )
    total_unique = sum(len(cs) for cs in selected_by_project.values())
    lines.append(f"- total_unique_cards (after dedup across projects): {total_unique}")
    lines.append("")
    return lines


def _section_project_profiles(
    project_names: list[str],
    profiles: dict[str, ProjectProfile],
) -> list[str]:
    """3. Project Profiles — 每项目的 description + preferred_workflow。"""
    lines = ["## Project Profiles"]
    for name in project_names:
        prof = profiles[name]
        lines.append(f"### {name}")
        if not prof.found:
            lines.append("- (no profile configured)")
            lines.append("")
            continue
        lines.append(f"- description: {prof.description or 'No description configured.'}")
        lines.append("- preferred_workflow:")
        if prof.preferred_workflow:
            for w in prof.preferred_workflow:
                lines.append(f"  - {w}")
        else:
            lines.append("  - No preferred_workflow configured.")
        lines.append("")
    return lines


def _section_cross_tracks(all_selected: list[CardSummary]) -> list[str]:
    """4. Cross-project Learning Tracks — 累计 track 计数（按数量降序）。"""
    lines = ["## Cross-project Learning Tracks"]
    track_counts: dict[str, int] = {}
    for c in all_selected:
        if c.track:
            track_counts[c.track] = track_counts.get(c.track, 0) + 1
    if not track_counts:
        lines.append("- (no learning_track found across selected cards)")
    else:
        for t, n in sorted(track_counts.items(), key=lambda kv: (-kv[1], kv[0])):
            lines.append(f"- {t} ({n} card{'s' if n != 1 else ''})")
    lines.append("")
    return lines


def _section_cross_principles(
    project_names: list[str],
    profiles: dict[str, ProjectProfile],
    selected_by_project: dict[str, list[CardSummary]],
) -> list[str]:
    """5. Cross-project Principles — 项目级 + 卡片级并列展示，不自动裁决。"""
    lines = ["## Cross-project Principles"]
    any_principles = False
    for name in project_names:
        for p in profiles[name].principles:
            lines.append(f"- from project [{name}]: {p}")
            any_principles = True
    for name in project_names:
        for c in selected_by_project.get(name, []):
            cid = c.id or c.path.stem
            for p in c.principles:
                lines.append(f"- from card [{cid}] (project {name}): {p}")
                any_principles = True
    if not any_principles:
        lines.append("- No principles found at project- or card-level.")
    lines.append("")
    return lines


def _section_cross_risks(
    project_names: list[str],
    profiles: dict[str, ProjectProfile],
    selected_by_project: dict[str, list[CardSummary]],
) -> list[str]:
    """6. Cross-project Known Risks — 项目级 + 卡片级风险并列。"""
    lines = ["## Cross-project Known Risks"]
    any_risks = False
    for name in project_names:
        for r in profiles[name].known_risks:
            lines.append(f"- from project [{name}]: {r}")
            any_risks = True
    for name in project_names:
        for c in selected_by_project.get(name, []):
            cid = c.id or c.path.stem
            for r in c.known_risks:
                lines.append(f"- from card [{cid}] (project {name}): {r}")
                any_risks = True
    if not any_risks:
        lines.append("- No known_risks found at project- or card-level.")
    lines.append("")
    return lines


def _section_project_specific_cards(
    project_names: list[str],
    selected_by_project: dict[str, list[CardSummary]],
) -> list[str]:
    """7. Project-specific Cards — 去重后按项目分组；空项目仍显式展示。"""
    lines = ["## Project-specific Cards"]
    for name in project_names:
        cs = selected_by_project.get(name, [])
        lines.append(f"### {name} ({len(cs)} card{'s' if len(cs) != 1 else ''})")
        if not cs:
            lines.append("- (no cards matched)")
            lines.append("")
            continue
        for c in cs:
            cid = c.id or c.path.stem
            lines.append(
                f"- [{cid}] {c.title or '(untitled)'} · "
                f"track: {c.track or '-'} · status: {c.status} · "
                f"value_score: {c.value_score if c.value_score is not None else '-'}"
            )
            lines.append(f"  - path: {c.rel_path}")
        lines.append("")
    return lines


def _section_shared_action_items(all_selected: list[CardSummary]) -> list[str]:
    """8. Shared Action Items — 跨项目按卡片合并的 action 列表。"""
    lines = ["## Shared Action Items"]
    aggregated: list[str] = []
    for c in all_selected:
        body = _safe_body(c.path)
        if not body:
            continue
        seg = extract_section(body, "Action Items")
        if seg:
            cid = c.id or c.path.stem
            aggregated.append(f"- from [{cid}]:\n{_indent(seg)}")
    if not aggregated:
        lines.append("- (no action items)")
    else:
        lines.extend(aggregated)
    lines.append("")
    return lines


def _section_review_due(all_selected: list[CardSummary]) -> list[str]:
    """9. Review Due — 跨项目复习提醒。"""
    lines = ["## Review Due"]
    due = _due_now(all_selected)
    if not due:
        lines.append("- (none due)")
    else:
        for c in due:
            cid = c.id or c.path.stem
            overdue = _overdue_days(c.review_after)
            tail = f" (overdue {overdue} days)" if overdue > 0 else " (due now)"
            lines.append(f"- [{cid}]{tail}")
    lines.append("")
    return lines


def _section_suggested_prompt(
    project_names: list[str],
    profiles: dict[str, ProjectProfile],
    options: ProjectContextOptions,
) -> list[str]:
    """10. Suggested Prompt — 固定模板拼装 next-step prompt。"""
    return [
        f"## Suggested Prompt for {options.target}\n",
        "```text",
        _render_multi_suggested_prompt(project_names, options.target, profiles),
        "```",
        "",
    ]


def _section_excluded_content() -> list[str]:
    """11. Excluded Content — 安全清单（共享自单 project 渲染器）。"""
    lines = ["## Excluded Content (safety guarantee)"]
    for item in EXCLUDED_CONTENT:
        lines.append(f"- {item}")
    return lines


def render_multi_project_context_json(
    project_names: list[str],
    cards_by_project: dict[str, list[CardSummary]],
    profiles: dict[str, ProjectProfile],
    *,
    options: ProjectContextOptions,
) -> str:
    """多 project JSON v2 渲染（schema 与单项目 v2 兼容，结构按多项目展开）。"""
    assert len(project_names) >= 2

    selected_by_project = _select_and_dedup(project_names, cards_by_project, options)
    all_selected = [c for cs in selected_by_project.values() for c in cs]
    now_iso = datetime.now(timezone.utc).astimezone().isoformat(timespec="seconds")

    track_counts: dict[str, int] = {}
    for c in all_selected:
        if c.track:
            track_counts[c.track] = track_counts.get(c.track, 0) + 1

    payload = {
        "version": 2,
        "mode": "multi_project",
        "projects": project_names,
        "project_count": len(project_names),
        "generated_at": now_iso,
        "target": options.target,
        "include_drafts": options.include_drafts,
        "include_prompts": options.include_prompts,
        "include_actions": options.include_actions,
        "include_review_due": options.include_review_due,
        "include_next_step_prompt": options.include_next_step_prompt,
        "total_unique_cards": sum(len(cs) for cs in selected_by_project.values()),
        "project_profiles": [
            {
                "project": name,
                "found": profiles[name].found,
                "rel_path": profiles[name].rel_path,
                "description": profiles[name].description,
                "preferred_workflow": list(profiles[name].preferred_workflow),
                "principles": list(profiles[name].principles),
                "known_risks": list(profiles[name].known_risks),
            }
            for name in project_names
        ],
        "cross_project_learning_tracks": [
            {"track": t, "count": n}
            for t, n in sorted(track_counts.items(), key=lambda kv: (-kv[1], kv[0]))
        ],
        "cross_project_principles": _collect_cross_principles(
            project_names, profiles, selected_by_project, "principles"
        ),
        "cross_project_known_risks": _collect_cross_principles(
            project_names, profiles, selected_by_project, "known_risks"
        ),
        "project_specific_cards": [
            {
                "project": name,
                "count": len(selected_by_project.get(name, [])),
                "items": [_card_json_min(c) for c in selected_by_project.get(name, [])],
            }
            for name in project_names
        ],
        "suggested_prompt": _render_multi_suggested_prompt(
            project_names, options.target, profiles
        ),
        "excluded_content": list(EXCLUDED_CONTENT),
    }
    return json.dumps(payload, ensure_ascii=False, indent=2, sort_keys=False)


