def _select_and_dedup(
    project_names: list[str],
    cards_by_project: dict[str, list[CardSummary]],
    options: ProjectContextOptions,
) -> dict[str, list[CardSummary]]:
    """按用户给的项目顺序对卡片做 path-level 去重；先到先得。

    幂等性：相同输入产出顺序稳定（依赖 ``_select`` 的稳定排序）。
    """
    seen: set[str] = set()
    out: dict[str, list[CardSummary]] = {}
    for name in project_names:
        cards = cards_by_project.get(name, [])
        selected = _select(cards, options)
        kept: list[CardSummary] = []
        for c in selected:
            key = str(c.path)
            if key in seen:
                continue
            seen.add(key)
            kept.append(c)
        out[name] = kept
    return out


def _collect_cross_principles(
    project_names: list[str],
    profiles: dict[str, ProjectProfile],
    selected_by_project: dict[str, list[CardSummary]],
    field_name: str,
) -> list[dict]:
    """聚合跨项目的 principles / known_risks，每条标注 source（project / card）。"""
    out: list[dict] = []
    for name in project_names:
        prof = profiles[name]
        items = getattr(prof, field_name, ())
        for it in items:
            out.append({"source_kind": "project", "source_id": name, "item": it})
    for name in project_names:
        for c in selected_by_project.get(name, []):
            cid = c.id or c.path.stem
            items = getattr(c, field_name, ())
            for it in items:
                out.append(
                    {
                        "source_kind": "card",
                        "source_id": cid,
                        "project": name,
                        "item": it,
                    }
                )
    return out


def _render_multi_suggested_prompt(
    project_names: list[str],
    target: str,
    profiles: dict[str, ProjectProfile],
) -> str:
    """多项目 suggested prompt（仍是固定模板拼装；**绝不**调 LLM）。

    与单项目模板的区别：明确声明这是 "multi-project context pack"，原则
    可能并存且**未自动裁决**，要求下游 agent 在用之前先确认是否冲突。
    """
    target_norm = target if target in _TARGET_STYLE else "generic"
    parts: list[str] = []
    parts.append(
        "你拿到的是 mindforge 生成的 **multi-project context pack**。\n"
        f"涉及项目：{', '.join(project_names)}（共 {len(project_names)} 个）；"
        f"target={target_norm}。\n"
        "\n"
        "工作前请按以下规则处理：\n"
        "\n"
        "1. 上下文里同时包含多个项目的 principles 与 known_risks，"
        "**MindForge 不自动裁决冲突**；如发现两个项目的原则相互矛盾，请先停下来"
        "向我确认本次任务以哪个项目为主；\n"
        "2. 不要捏造没有出现在上下文中的事实；\n"
        "3. 你的产出要明确区分"
        "「来自上下文的事实」与「你的推断」；\n"
        "4. 不要试图读取或修改 .env / runs / state.json；\n"
        "5. 涉及代码改动时，先列文件路径、再给 diff 或最小补丁；\n"
        "6. 如果你的改动只关心其中某一个项目，请在产出开头声明 "
        '"focused_project: <name>"，避免把跨项目原则当成单项目结论。'
    )
    parts.append("")
    parts.append(_TARGET_STYLE[target_norm])

    # 仅展示 found=true 的项目级 principles / preferred_workflow
    for name in project_names:
        prof = profiles[name]
        if not prof.found:
            continue
        if prof.principles:
            parts.append("")
            parts.append(f"项目 [{name}] 的 principles：")
            parts.extend(f"- {p}" for p in prof.principles)
        if prof.preferred_workflow:
            parts.append("")
            parts.append(f"项目 [{name}] 的 preferred_workflow：")
            parts.extend(f"- {w}" for w in prof.preferred_workflow)

    return "\n".join(parts)


def _card_json_min(c: CardSummary) -> dict:
    """多项目 JSON 输出仅展示卡片的安全元数据（不含正文段落）。"""
    return {
        "id": c.id,
        "title": c.title,
        "path": c.rel_path,
        "status": c.status,
        "track": c.track,
        "projects": list(c.projects),
        "tags": list(c.tags),
        "source_type": c.source_type,
        "source_url": c.source_url,
        "value_score": c.value_score,
        "reviewed_at": c.reviewed_at.isoformat() if c.reviewed_at else None,
        "review_after": c.review_after.isoformat() if c.review_after else None,
        "principles": list(c.principles),
        "known_risks": list(c.known_risks),
    }


def _indent(s: str, prefix: str = "  ") -> str:
    return "\n".join(prefix + line for line in s.splitlines())


