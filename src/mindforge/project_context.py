def render_project_context_json(
    project_name: str,
    cards: list[CardSummary],
    *,
    options: ProjectContextOptions,
    profile: ProjectProfile | None = None,
) -> str:
    profile = profile or ProjectProfile(
        project_name=project_name, found=False, rel_path=None
    )
    selected = _select(cards, options)
    now_iso = datetime.now(timezone.utc).astimezone().isoformat(timespec="seconds")
    suggested_prompt = _render_suggested_prompt(
        project_name, options.target, profile
    )

    payload = {
        "version": 2,
        "project": project_name,
        "generated_at": now_iso,
        "target": options.target,
        "include_drafts": options.include_drafts,
        "include_prompts": options.include_prompts,
        "include_actions": options.include_actions,
        "include_review_due": options.include_review_due,
        "include_next_step_prompt": options.include_next_step_prompt,
        "count": len(selected),

        "project_profile_found": profile.found,
        "project_profile_path": profile.rel_path,
        "project_description": profile.description,
        "preferred_workflow": list(profile.preferred_workflow),
        "project_level_principles": list(profile.principles),
        "card_level_principles": [
            {"card_id": cid, "items": list(items)}
            for cid, items in _collect_card_field(selected, "principles")
        ],
        "project_level_known_risks": list(profile.known_risks),
        "card_level_known_risks": [
            {"card_id": cid, "items": list(items)}
            for cid, items in _collect_card_field(selected, "known_risks")
        ],

        "items": [_card_json(c, include_prompts=options.include_prompts) for c in selected],
        "suggested_prompt": suggested_prompt,
        "excluded_content": list(EXCLUDED_CONTENT),
    }
    return json.dumps(payload, ensure_ascii=False, indent=2, sort_keys=False)


# ---------------------------------------------------------------------------
# Suggested Prompt 模板（**固定文本，绝不调 LLM**）
# ---------------------------------------------------------------------------

_TARGET_STYLE: dict[str, str] = {
    "claude-code": (
        "工作风格指令（claude-code）：\n"
        "- 先看真实事件链路（日志 / checkpoint / runtime_observer / state\n"
        "  / 真实 events）再决定改不改代码；\n"
        "- 不要只根据 UI 表象打补丁；\n"
        "- 产出 3 步建议：next-action / verification / risk。"
    ),
    "copilot": (
        "工作风格指令（copilot）：\n"
        "- 先 plan（列出受影响文件 + 测试范围），再小步实现；\n"
        "- 每个改动后必须给出验证命令（pytest / ruff / smoke）；\n"
        "- 遇到不确定的设计点，先给 2–3 个选项与 trade-off 让我选，再实施。"
    ),
    "codex": (
        "工作风格指令（codex）：\n"
        "- 仅提交「高质量补丁 + 测试」；\n"
        "- 不引入未列出的依赖；不做超出请求范围的重构；\n"
        "- 如发现 root cause 与请求不一致，先停下来汇报再继续。"
    ),
    "generic": (
        "工作风格指令（generic）：\n"
        "- 通用安全开发版本：plan → patch → test → report；\n"
        "- 任何破坏性操作（删文件 / 改 schema / 改 lockfile）必须先确认。"
    ),
}


def _render_suggested_prompt(
    project_name: str, target: str, profile: ProjectProfile
) -> str:
    """按 §4 拼装 suggested prompt。**纯字符串拼接**，无 LLM。"""
    target_norm = target if target in _TARGET_STYLE else "generic"
    parts: list[str] = []
    parts.append(
        f"你是我在 {project_name} 项目上的协作助手（target={target_norm}）。\n"
        "mindforge 已经把已审核的项目上下文打包给你（project profile +\n"
        "卡片摘要 + 行动项 + 风险 + 复习清单）。请严格按以下规则工作：\n"
        "\n"
        "1. 不要捏造没有出现在上下文中的事实；\n"
        "2. 你的产出要明确区分「来自上下文的事实」与「你的推断」；\n"
        "3. 不要试图读取或修改 .env / runs / state.json；\n"
        "4. 涉及代码改动时，先列文件路径、再给 diff 或最小补丁；\n"
        "5. 如果项目 profile 列出了 principles / known_risks，请优先遵守。"
    )
    parts.append("")
    parts.append(_TARGET_STYLE[target_norm])

    if profile.principles:
        parts.append("")
        parts.append("项目级 principles：")
        parts.extend(f"- {p}" for p in profile.principles)
    if profile.preferred_workflow:
        parts.append("")
        parts.append("项目级 preferred_workflow：")
        parts.extend(f"- {w}" for w in profile.preferred_workflow)

    return "\n".join(parts)


